def bubble_sort(l):
    swap = True
    while swap:
        # print(l)
        swap=False
        for i in range(len(l)-1):
            if l[i] > l[i+1]:
                swap=True
                l[i], l[i+1] = l[i+1], l[i]
    print(l)


l = [6,8,1,4,10,7,8,9,3,2,5]

bubble_sort(l)

